export function processCommand(input: string): string {
  const parts = input.trim().toLowerCase().split(/\s+/);
  const cmd = parts[0];
  const args = parts.slice(1);

  if (cmd === "clear") return "";

  if (cmd === "blog" && args.length > 0) {
    const id = parseInt(args[0]);
    const post = BLOG_POSTS.find((p) => p.id === id);
    if (post) {
      return `\n[${post.date}] ${post.title}\n${"─".repeat(50)}\n${post.body}\n`;
    }
    return `\n  ERROR: Blog entry #${args[0]} not found.\n  Use "blog" to list available entries.\n`;
  }

  if (COMMANDS[cmd]) {
    return COMMANDS[cmd]();
  }

  return `\n  ERROR: Command not recognized: "${input}"\n  Type "help" for available commands.\n`;
}
